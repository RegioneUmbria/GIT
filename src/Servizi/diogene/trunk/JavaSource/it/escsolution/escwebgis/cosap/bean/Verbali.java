package it.escsolution.escwebgis.cosap.bean;

public class Verbali {

	private String tipoOccupazione;
	private String codOggetto;
	private String indirizzo;
	private String civico;
	private String annoRiferimento;
	private String numeroProtocollo;
	private String annoProtocollo;
	

	public String getTipoOccupazione() {
		return tipoOccupazione;
	}
	public void setTipoOccupazione(String tipoOccupazione) {
		this.tipoOccupazione = tipoOccupazione;
	}
	public String getCodOggetto() {
		return codOggetto;
	}
	public void setCodOggetto(String codOggetto) {
		this.codOggetto = codOggetto;
	}
	public String getIndirizzo() {
		return indirizzo;
	}
	public void setIndirizzo(String indirizzo) {
		this.indirizzo = indirizzo;
	}
	public String getCivico() {
		return civico;
	}
	public void setCivico(String civico) {
		this.civico = civico;
	}
	public String getAnnoRiferimento() {
		return annoRiferimento;
	}
	public void setAnnoRiferimento(String annoRiferimento) {
		this.annoRiferimento = annoRiferimento;
	}
	public String getNumeroProtocollo() {
		return numeroProtocollo;
	}
	public void setNumeroProtocollo(String numeroProtocollo) {
		this.numeroProtocollo = numeroProtocollo;
	}
	public String getAnnoProtocollo() {
		return annoProtocollo;
	}
	public void setAnnoProtocollo(String annoProtocollo) {
		this.annoProtocollo = annoProtocollo;
	}
	
}
