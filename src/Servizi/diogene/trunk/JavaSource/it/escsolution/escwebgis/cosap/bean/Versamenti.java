package it.escsolution.escwebgis.cosap.bean;

public class Versamenti {

	private String numeroVersamento;
	private String annoVersamento;
	private String dataVersamento;
	private String importoVersamento;
	
	public String getNumeroVersamento() {
		return numeroVersamento;
	}
	public void setNumeroVersamento(String numeroVersamento) {
		this.numeroVersamento = numeroVersamento;
	}
	public String getAnnoVersamento() {
		return annoVersamento;
	}
	public void setAnnoVersamento(String annoVersamento) {
		this.annoVersamento = annoVersamento;
	}
	public String getDataVersamento() {
		return dataVersamento;
	}
	public void setDataVersamento(String dataVersamento) {
		this.dataVersamento = dataVersamento;
	}
	public String getImportoVersamento() {
		return importoVersamento;
	}
	public void setImportoVersamento(String importoVersamento) {
		this.importoVersamento = importoVersamento;
	}
	
}
