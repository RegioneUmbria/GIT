package it.webred.ct.data.access.basic.pregeo;

public class PregeoServiceException extends RuntimeException {

	public PregeoServiceException() {
		super();
		
	}

	public PregeoServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	
	}

	public PregeoServiceException(String arg0) {
		super(arg0);
		
	}

	public PregeoServiceException(Throwable arg0) {
		super(arg0);
		
	}

}
