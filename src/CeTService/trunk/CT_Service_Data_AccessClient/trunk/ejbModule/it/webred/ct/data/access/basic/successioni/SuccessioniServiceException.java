package it.webred.ct.data.access.basic.successioni;

public class SuccessioniServiceException extends RuntimeException {

	private static final long serialVersionUID = -1180293035895169621L;

	public SuccessioniServiceException() {
		super();
	}

	public SuccessioniServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SuccessioniServiceException(String arg0) {
		super(arg0);
	}

	public SuccessioniServiceException(Throwable arg0) {
		super(arg0);
	}

}
