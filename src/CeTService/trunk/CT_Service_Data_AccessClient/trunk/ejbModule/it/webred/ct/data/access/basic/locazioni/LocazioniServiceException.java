package it.webred.ct.data.access.basic.locazioni;

public class LocazioniServiceException extends RuntimeException {

	private static final long serialVersionUID = -1180293035895169621L;

	public LocazioniServiceException() {
		super();
	
	}

	public LocazioniServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public LocazioniServiceException(String arg0) {
		super(arg0);
		
	}

	public LocazioniServiceException(Throwable arg0) {
		super(arg0);
		
	}

}
